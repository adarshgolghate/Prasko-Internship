//initializing of the class
//Expected output should be: DooBeeDooBeeDo
public class DooBee {
    //declaring of main method
    public static void main(String args[])
    {
        //initializing of the variable x.
        int x =1;
        //running a while loop and it will run till the value of x is less than 3
        while(x<3)
        {
            System.out.print("Doo");
            System.out.print("Bee");
            //adding one to the previous value of x.
            x=x+1;
        }
        //decalred a if statement.
        if(x==3)
        {
            System.out.print("Do");
        }
    }
    
}
